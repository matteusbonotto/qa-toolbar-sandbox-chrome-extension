  const originals = new WeakMap();
  const attributeOriginals = new WeakMap();
  const attributeNames = ["placeholder", "title", "aria-label"];

  function translateText(text, locale, replacements = {}) {
    let translated = locale === "pt-BR" ? text : translations[locale]?.[text] || text;
    for (const [key, value] of Object.entries(replacements)) translated = translated.replaceAll(`{${key}}`, String(value));
    return translated;
  }

  function translateTextNode(node, locale) {
    const source = originals.get(node) ?? node.nodeValue;
    originals.set(node, source);
    const leading = source.match(/^\s*/)?.[0] || "";
    const trailing = source.match(/\s*$/)?.[0] || "";
    const value = source.trim();
    if (value) node.nodeValue = `${leading}${translateText(value, locale)}${trailing}`;
  }

  function translateElement(element, locale) {
    const stored = attributeOriginals.get(element) || {};
    for (const attribute of attributeNames) {
      if (!element.hasAttribute(attribute)) continue;
      const source = stored[attribute] ?? element.getAttribute(attribute);
      stored[attribute] = source;
      element.setAttribute(attribute, translateText(source, locale));
    }
    attributeOriginals.set(element, stored);
  }

  function apply(locale = "pt-BR", root = document) {
    document.title = translateText("QA Toolbar Sandbox — Configurações", locale);
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT);
    let node = walker.currentNode;
    while (node) {
      if (node.nodeType === Node.TEXT_NODE) translateTextNode(node, locale);
      else if (node.nodeType === Node.ELEMENT_NODE) translateElement(node, locale);
      node = walker.nextNode();
    }
  }

  window.QTS_OPTIONS_I18N = { apply, translateText };
