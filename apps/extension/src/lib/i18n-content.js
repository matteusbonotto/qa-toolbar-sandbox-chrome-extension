  function detectDefaultLocale() {
    const uiLang = (chrome.i18n?.getUILanguage?.() || navigator.language || "pt-BR").toLowerCase();
    if (uiLang.startsWith("es")) return "es";
    if (uiLang.startsWith("en")) return "en";
    return "pt-BR";
  }

  async function getLocale() {
    const stored = await chrome.storage.local.get(LOCALE_KEY);
    const value = stored[LOCALE_KEY];
    return SUPPORTED.includes(value) ? value : detectDefaultLocale();
  }

  async function setLocale(locale) {
    if (!SUPPORTED.includes(locale)) return;
    await chrome.storage.local.set({ [LOCALE_KEY]: locale });
  }

  async function load() {
    const locale = await getLocale();
    return MESSAGES[locale];
  }

  window.QTS_I18N = { SUPPORTED, getLocale, setLocale, load, LOCALE_KEY };
